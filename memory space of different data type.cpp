#include<iostream>
using namespace std;
int main()
{
    cout<<"Here Are A Representation of C++ Data Types And Their Memory Space In Bytes\n\n";

    cout<<"size of boolean "<<sizeof(bool)<<"\n\n";
    cout<<"size of a char "<<sizeof(char)<<"\n\n";
    cout<<"size of a int "<<sizeof(int)<<"\n\n";
    cout<<"size of short int "<<sizeof(short int)<<"\n\n";
    cout<<"size of signed int "<<sizeof(signed int)<<"\n";
    cout<<"size of unsigned int "<<sizeof(unsigned int)<<"\n\n";
    cout<<"size of a long int "<<sizeof(long int)<<"\n\n";
    cout<<"size of a long long int "<<sizeof(long long int)<<"\n\n";
    cout<<"size of float "<<sizeof(float)<<"\n\n";
    cout<<"size of double "<<sizeof(double)<<"\n\n";
    cout<<"size of long double "<<sizeof(long double)<<"\n\n";
    cout<<"size of wchar_t "<<sizeof(wchar_t)<<"\n\n";
    return 0;
}
